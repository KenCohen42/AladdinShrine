 Meeting Date: Third Thursday<p>
  The Registrar Unit, commonly known as &quot; The
  Gold Coats&quot;, was elevated from status of a Committee to that of a Unit
  in 1967 by the Illustrious Sir, Potentate Mark W. Bobb.
  Since its humble beginnings, the Registrar Unit has become one of the premier
  uniformed units in the Aladdin Shrines in fulfilling its prime objective of
  service to the Center, its Illustrious Potentate, and Shrinedom
  while enjoying good Masonic Fellowship.<p>
  
  The Registrar Unit fulfills its prime service objective by conducting
  registration, verifying current dues cards, and admitting the nobility to
  center functions such as ceremonials, picnics, dances, and other functions as
  directed. During the Annual Shrine Circus, the Unit admits the general public
  through the various gates to all performances and counts all ticket stubs so
  that an accurate attendance count may be obtained. It also functions as a
  marching unit in parades where Aladdin Shrine Center
  is officially represented. <p>
  
  The Unit membership consists of 70 active, 15 apprentices, and varying
  number of veteran members, all nobles in good standing of Aladdin Shrine. The
  Unit conducts a business meeting on the third Thursday of each month except
  July, August, and December. 
