            <p align="left">The Ohio Shrine Association was organized in 1981. 
			  It holds its annual business meeting on the first weekend in March 
			  in the home city of the President's Shrine Center.</p>

            <p align="left">The organization of six Ohio Shrine Centers promotes 
              cordiality and friendship, it harmonizes resolutions of territorial 
              and jurisdictional questions and the exchange of affirmative ideas 
              regarding membership, uniformity of fees and dues and Shrine club 
              relationships.</p>

            <p align="left">This year Shriners of Ohio will again hold their annual 
              newspaper distribution to raise money for the Shriners 22 Orthopedic 
              and Burns Hospitals for Children. The project dubbed <b>SO HELP</b> 
              - <b>S</b>hriners of <b>O</b>hio <b>H</b>ospital <b>E</b>dition 
              <b>L</b>ocal <b>P</b>aper.</p>

            <p align="left">Several Nobles were skeptical as they worked diligently 
              on preparations for the initial program in 1978. But the donations 
              came in and the four-page tabloid was a big success. The Ohio Shrine 
              Association gave the Hospitals a check for $150,000. as a result 
              of the first state-wide effort.</p>

