<h1 align="center">Great Lakes Shrine Association</h1>

<p>The Great Lakes Shrine Association is made up of 23 Centers in five states and the Canadian Province of Ontario. An Annual business meeting is held each
February. A ceremonial session is conducted each September and is attended by thousands of Nobles from the member Shrine Centers.

<p>Inter-Center relationships are maintained throughout the year through correspondence and other contacts, resulting in a high degree of Shrine achievement in
the Great Lakes Region.</p>

